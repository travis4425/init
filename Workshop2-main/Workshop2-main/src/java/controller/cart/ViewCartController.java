package controller.cart;

import dao.CartDAO;
import dao.ProductDAO;
import dto.CartDetail;
import dto.Category;
import dto.User;
import java.io.IOException;
import jakarta.servlet.ServletException;
import jakarta.servlet.annotation.WebServlet;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import jakarta.servlet.http.HttpSession;
import java.util.List;

/**
 *
 * @author Admin
 */
@WebServlet(name="ViewCartController", urlPatterns={"/ViewCartController"})
public class ViewCartController extends HttpServlet {
   
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        HttpSession session = request.getSession();
        User loginUser = (User) session.getAttribute("LOGIN_USER");
        if (loginUser == null) {
            response.sendRedirect("login.jsp");
            return;
        }
        String userID = loginUser.getUserID();
        CartDAO cartDAO = new CartDAO();
        ProductDAO dao = new ProductDAO();
        try {
            List<CartDetail> cartItems = cartDAO.viewCart(userID);
            List<Category> categories = dao.getAllCategories();
            
            request.setAttribute("categories", categories);
            request.setAttribute("list", cartItems);
            request.getRequestDispatcher("cart/cartList.jsp").forward(request, response);
        } catch (Exception e) {
            e.printStackTrace();
        }
    } 

    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        processRequest(request, response);
    } 

    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        processRequest(request, response);
    }


    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
